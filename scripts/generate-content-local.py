def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('--only', help='カンマ区切りで特定スラグのみ処理')
    args = parser.parse_args()

    with open(CITIES_PATH, encoding='utf-8') as f:
        cities = json.load(f)

    only_set = set(args.only.split(',')) if args.only else None
    targets  = [s for s in cities if (only_set is None or s in only_set)]

    print(f'処理対象: {len(targets)} 都市')
    updated = 0
    for slug in targets:
        entry = cities[slug]
        ctx   = CITY_CONTEXT.get(slug, {})
        content = generate(slug, entry, ctx)
        for k, v in content.items():
            entry[k] = v
        updated += 1
        if updated % 20 == 0:
            with open(CITIES_PATH, 'w', encoding='utf-8') as f:
                json.dump(cities, f, ensure_ascii=False, indent=2)
            print(f'  [中間保存] {updated} 都市完了')

    with open(CITIES_PATH, 'w', encoding='utf-8') as f:
        json.dump(cities, f, ensure_ascii=False, indent=2)

    print(f'\n✓ 完了: {updated} 都市にコンテンツ適用')

if __name__ == '__main__':
    main()
