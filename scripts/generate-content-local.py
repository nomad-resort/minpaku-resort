def classify(slug, ctx):
    t = ctx.get('type', '')
    n = ctx.get('notes', '')
    g = ctx.get('guests', '')
    combo = t + n + g

    if '国際スキー' in t: return 'intl_ski'
    if 'スキー' in combo: return 'ski'
    if '世界遺産' in combo and ('温泉' not in t): return 'heritage'
    if '古都' in t or '城下町' in t or '歴史' in t or '異国情緒' in t or '城' in t or '美観' in t: return 'historic'
    if '秘境温泉' in t or '高級温泉' in t: return 'luxury_onsen'
    if '温泉' in t and 'リゾート' in t: return 'onsen_resort'
    if '温泉' in t: return 'onsen'
    if '離島' in t: return 'island'
    if '富士' in combo: return 'fuji'
    if '政令市' in t: return 'major_city'
    if '都市型' in t or '県都' in t or '国際観光都市' in t or '下町' in t: return 'urban'
    if 'しまなみ' in combo or 'サイクリング' in t or '坂の街' in t: return 'cycling_coastal'
    if 'サーフィン' in combo or 'サーファー' in combo or '海岸' in t or 'ビーチ' in t: return 'coastal'
    if '自然' in t or 'アウトドア' in t or '農村' in t or '山岳' in t: return 'nature'
    if '空港' in combo: return 'gateway'
    if 'おどり' in combo or 'ねぶた' in combo or 'くんち' in combo or '祭り' in n or 'まつり' in n: return 'festival'
    return 'regional'

