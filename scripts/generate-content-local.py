    rules = RULES.get(city_type, RULES['regional'])
    result = [dict(r) for r in rules]

    # historic の rule[2] を各都市の peak データで動的生成
    if city_type == 'historic':
        peak = ctx.get('peak', '春・秋')
        season_count = sum(1 for s in ['春', '夏', '秋', '冬'] if s in peak)
        if '通年' in peak:
            peak_label = '通年・ピークシーズン'
        elif season_count >= 3:
            peak_label = '四季を通じた各シーズン'
        else:
            peak_label = peak.split('・')[0].split('（')[0] + 'シーズン'
        result[2] = {
            'icon': 'fas fa-calendar',
            'title': f'{peak_label}の集中需要を逃さない清掃体制',
            'body': f'{peak}に観光客が集中するエリアでは、繁忙期の即日複数清掃体制と直前予約への柔軟な対応で機会損失を防ぎます。繁忙期の収益機会を最大限に確保します。',
            'highlight': '繁忙期対応',
        }

    # regional の rule[1]/rule[2] をゲスト層・シーズンで動的生成
    elif city_type == 'regional':
        guests = ctx.get('guests', '国内旅行者')
        peak   = ctx.get('peak', '通年')

        # rule[1]: ゲスト層に応じた対応
